import os
import time
import argparse

parser = argparse.ArgumentParser(description ='print any text file')
parser.add_argument('-r', '--repeat', help = "how many times should the program print? (default = 1)", type=int)
parser.add_argument('-s', '--speed', help = "speed of printing (default 0.1, higher value = lower speed)", type=float)
parser.add_argument('-i', '--infinite', help = "print infinitely (CTRL+C stops printing)", action = "store_true")
parser.add_argument('-f', '--file', help = "select text file", type=str)
args = parser.parse_args()

def myascii(filename):
    myfile = open(filename, 'r')
    lines = myfile.readlines()

    if args.speed: s = args.speed 
    else: s = 0.1 

    if args.repeat: r = args.repeat + 1
    else: r = 2
   
    c = 1
    
    while c < r:
        for line in lines:
            print(line.strip('\n'))
            time.sleep(s)
        print('\n')
        c = c + 1
        
        if args.infinite: r = r + 1
    
    myfile.close()

if args.file:
   f = args.file
   myascii(f)
else:
    print("please specify a text file using -f/--file option.")



















































































































































































































































































































































































































































































































































































































































































































































































